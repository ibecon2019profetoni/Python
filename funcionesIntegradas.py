# Funciones Integradas
# ********************

# convertir un string a entero
n = int('10')

# convertir un string a decimal
n = float('10.5')

# convertir un número a string
n = str(10)

# convertir un número a binario
n = bin(10)

# convertir un número a hexadecimal
n = hex(10)

# convertir un binario a entero
n = int('0b1010', 2)

# convetir un hexadecimal a entero
n = int('0xa', 16)

# convetir un negativo a positivo
n = abs(-12)

# redondear un número decimal a entero
n = round(4.9)

# cuenta cuantos carácteres tiene un string
n = len('Toni')





