# Ecuación de Pitágoras
# *********************
# a**2 + b**2 = c**2

# recogemos datos
a = float(input('a -->'))
b = float(input('b -->'))
suma = (a**2) + (b**2)
resultado = suma**0.5

print(f"Resultado: {resultado:.2f}")


# Calcular el área y longitud de un circulo
# *****************************************
# area = pi * r**2
# longitud = 2 * pi * r

import math

radio = float(input('radio ->'))
area = math.pi * radio**2
longitud = 2 * math.pi * radio

print (f'Si el radio es: {radio:.2f} '
        f'el area tiene que ser {area:.2f} '
        f'y la longitud es {longitud:.2f}')







